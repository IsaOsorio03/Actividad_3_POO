    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        double a, b, c, x1, x2;
        a = Double.parseDouble(txtValorA.getText());
        b = Double.parseDouble(txtValorB.getText());
        c = Double.parseDouble(txtValorC.getText());
        
        x1 = (-b + Math.sqrt(Math.pow(b, 2) - 4 * a * c)) / (2 * a); 
        x2 = (-b - Math.sqrt(Math.pow(b, 2) - 4 * a * c)) / (2 * a);
        
        if (x1==x2) {
            txtXunica.setText(String.valueOf(x1));
            
        } else {
            txtX1.setText(String.valueOf(x1));
            txtX2.setText(String.valueOf(x2));
        }
        
       
    }//GEN-LAST:event_jButton1ActionPerformed

